import { useState, useEffect } from "react";
import { Card, CardContent, CardHeader, CardTitle } from "@/components/ui/card";
import { BarChart, Bar, XAxis, YAxis, CartesianGrid, Tooltip, ResponsiveContainer, PieChart, Pie, Cell } from "recharts";

const COLORS = [
  "hsl(168, 70%, 38%)",
  "hsl(43, 96%, 56%)",
  "hsl(262, 60%, 55%)",
  "hsl(200, 70%, 50%)",
  "hsl(340, 65%, 55%)",
];

const Analytics = () => {
  const [tasks, setTasks] = useState<any[]>([]);
  const [studySchedule, setStudySchedule] = useState<any[]>([]);

  useEffect(() => {
    setTasks(JSON.parse(localStorage.getItem("tasks") || "[]"));
    setStudySchedule(JSON.parse(localStorage.getItem("studySchedule") || "[]"));
  }, []);

  const completed = tasks.filter((t) => t.completed).length;
  const active = tasks.length - completed;

  const taskPieData = [
    { name: "Completed", value: completed },
    { name: "Active", value: active },
  ];

  const priorityData = [
    { name: "Low", count: tasks.filter((t) => t.priority === "low").length },
    { name: "Medium", count: tasks.filter((t) => t.priority === "medium").length },
    { name: "High", count: tasks.filter((t) => t.priority === "high").length },
  ];

  const studyBySubject: Record<string, number> = {};
  studySchedule.forEach((e: any) => {
    studyBySubject[e.subjectName] = (studyBySubject[e.subjectName] || 0) + e.hours;
  });
  const studyData = Object.entries(studyBySubject).map(([name, hours]) => ({ name, hours }));

  return (
    <div className="space-y-6">
      <div>
        <h1 className="text-3xl font-bold text-foreground">Analytics</h1>
        <p className="text-muted-foreground mt-1">Track your progress and performance.</p>
      </div>

      {tasks.length === 0 && studySchedule.length === 0 ? (
        <Card><CardContent className="py-12 text-center text-muted-foreground">No data yet. Add tasks or create a study plan to see analytics.</CardContent></Card>
      ) : (
        <div className="grid grid-cols-1 lg:grid-cols-2 gap-6">
          <Card>
            <CardHeader><CardTitle className="text-base">Task Status</CardTitle></CardHeader>
            <CardContent>
              {tasks.length > 0 ? (
                <ResponsiveContainer width="100%" height={250}>
                  <PieChart>
                    <Pie data={taskPieData} cx="50%" cy="50%" outerRadius={90} dataKey="value" label={({ name, value }) => `${name}: ${value}`}>
                      {taskPieData.map((_, i) => <Cell key={i} fill={COLORS[i]} />)}
                    </Pie>
                    <Tooltip />
                  </PieChart>
                </ResponsiveContainer>
              ) : <p className="text-center text-muted-foreground py-8">No tasks yet</p>}
            </CardContent>
          </Card>

          <Card>
            <CardHeader><CardTitle className="text-base">Tasks by Priority</CardTitle></CardHeader>
            <CardContent>
              {tasks.length > 0 ? (
                <ResponsiveContainer width="100%" height={250}>
                  <BarChart data={priorityData}>
                    <CartesianGrid strokeDasharray="3 3" stroke="hsl(210, 15%, 89%)" />
                    <XAxis dataKey="name" tick={{ fontSize: 12 }} />
                    <YAxis allowDecimals={false} tick={{ fontSize: 12 }} />
                    <Tooltip />
                    <Bar dataKey="count" fill={COLORS[0]} radius={[6, 6, 0, 0]} />
                  </BarChart>
                </ResponsiveContainer>
              ) : <p className="text-center text-muted-foreground py-8">No tasks yet</p>}
            </CardContent>
          </Card>

          <Card className="lg:col-span-2">
            <CardHeader><CardTitle className="text-base">Study Hours by Subject</CardTitle></CardHeader>
            <CardContent>
              {studyData.length > 0 ? (
                <ResponsiveContainer width="100%" height={280}>
                  <BarChart data={studyData}>
                    <CartesianGrid strokeDasharray="3 3" stroke="hsl(210, 15%, 89%)" />
                    <XAxis dataKey="name" tick={{ fontSize: 12 }} />
                    <YAxis tick={{ fontSize: 12 }} />
                    <Tooltip />
                    <Bar dataKey="hours" fill={COLORS[1]} radius={[6, 6, 0, 0]} />
                  </BarChart>
                </ResponsiveContainer>
              ) : <p className="text-center text-muted-foreground py-8">No study plan yet</p>}
            </CardContent>
          </Card>
        </div>
      )}
    </div>
  );
};

export default Analytics;
